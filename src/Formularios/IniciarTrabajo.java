    public void terminar(){
    
        
         if(fechainicio.getDate()!=null && id_lote > 0){
            
             int resp=JOptionPane.showConfirmDialog(null,"Desea Iniciar el Trabajo P/N = "+INF_PN.getText()+" Lote = "+INF_LOTE.getText());
          if (JOptionPane.OK_OPTION == resp){
          
        CTrabajos m = new CTrabajos();
        try {
           m.setId_lote(id_lote);
           m.setFechainicio(fechainicio.getDate());
           BDTrabajos.IniciarTra(m);
           JOptionPane.showMessageDialog(null, "Trabajo Iniciado Correctamente");
           limpiar();
          // LimpirTablaPro();
           ListarTrabajos();
           
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "MENSAJE"+e);
        }
        }
        }else{JOptionPane.showMessageDialog(null, "Ingrese Una Fecha o Seleccione Un Trabajo");}
    }

