    private void ListarTrabajos(){
        ArrayList<CTrabajos> result = BD.BDTrabajos.ListarTrabajos(PN.getText(),JOB.getText());
        RecargarTabla(result);  
    }
     private void RecargarTabla(ArrayList<CTrabajos> list) {
         
              Object[][] datos = new Object[list.size()][7];
              int i = 0;
              for(CTrabajos t : list)
              {
                  datos[i][0] = t.getId_trabajo();
                  datos[i][1] = t.getPN();
                  datos[i][2] = t.getJob();
                  datos[i][3] = t.getCliente();
                  datos[i][4] = t.getCantidadporlote();
                  datos[i][5] = t.getNo_lote();
                  datos[i][6] = t.getObservaciones();
                  i++;
              }    
             Trabajos.setModel(new javax.swing.table.DefaultTableModel(
                datos,
                new String[]{
                "No.","P/N","JOB","CLIENTE","CANTIDAD LOTE","NO LOTE","OBSERVACIONES"
             })
             {  
                 @Override
                 public boolean isCellEditable(int row, int column){
                 return false;
             }
             });
     }
    
    
