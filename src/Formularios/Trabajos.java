     public void obtenerID()
    {
        
        try {
             Connection cn = BDConexion.getConnection();
             Statement ps = cn.createStatement();
             ResultSet rs = ps.executeQuery("select max(ID_TRABAJO) from trabajos");
             rs.next();
             n = (rs.getInt("max(ID_TRABAJO)"));
             ps.close();
             rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error");
        }
          no.setText(String.valueOf(n+1));
      //  
    }
     
