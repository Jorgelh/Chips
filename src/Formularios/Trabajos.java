    private void BGUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BGUARDARActionPerformed
       
        if (PN.getText().compareTo("")!=0 && JOB.getText().compareTo("")!=0 && 
            CLIENTE.getText().compareTo("")!=0 && CANDELOTES.getText().compareTo("")!=0 &&
            CANPORLOTE.getText().compareTo("")!=0) {
            try {
                CTrabajos t = new CTrabajos();
                t.setId_trabajo(Integer.parseInt(no.getText()));
                t.setPN(PN.getText().toUpperCase());
                t.setJob(JOB.getText().toUpperCase());
                t.setCliente(CLIENTE.getText().toUpperCase());
                t.setCantidadlotes(Integer.parseInt(CANDELOTES.getText()));
                t.setCantidadporlote(Integer.parseInt(CANPORLOTE.getText()));
                t.setFechain(FECHAIN.getDate());
                t.setObservaciones(OBSERVA.getText().toUpperCase());
                t.setNotas(NOTAS.getText());
                BDTrabajos.InsertarTrabajo(t);
                crearlotes();
                limpiar();
            } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "error"+e);
            }
                        JOptionPane.showMessageDialog(null, "Trabajo Guardado");
        }
    }//GEN-LAST:event_BGUARDARActionPerformed

